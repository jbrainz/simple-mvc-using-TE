const mongoose = require("mongoose")

const Schema = mongoose.Schema

const ordersSchema = new Schema({
  products: [
    {
      product: {
        type: Object,
        required: true,
      },
      quantity: {
        type: Number,
        required: true,
      },
    },
  ],
  users: {
    name: {
      type: String,
      required: true,
    },
    userId: {
      type: Schema.Types.ObjectId,
      required: true,
      ref: "User",
    },
  },
})

module.exports = mongoose.model("Order", ordersSchema)
